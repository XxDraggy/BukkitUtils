package com.xxdraggy.utils.data.color;

public class RgbColor {
    private final int red;
    public int getRed() {
        return red;
    }

    private final int green;
    public int getGreen() {
        return green;
    }

    private final int blue;
    public int getBlue() {
        return blue;
    }

    public RgbColor(int red, int green, int blue) {
        this.red = red;
        this.green = green;
        this.blue = blue;
    }
}
